// FOR LOOP (Exercise 10 of 20)

// Define the variable total and set it to 0
let total = 0;

// Define the variable limit and set it to 10
let limit = 10;

// Create a for loop with a variable i starting at 0 and increasing by 1 each time through the loop
for (let i = 0; i < limit; i++) {
    // On each iteration of the loop, add the number i to the total variable
    total += i;
}

// Output the final value of the total variable
console.log( total);

